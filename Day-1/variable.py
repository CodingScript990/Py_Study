# Variable

# 변수는 일상에서 전화번호부 라고 생각하면 쉽다!
# 즉, 데이터가 필요할때 우리가 예시로 "엄마"에게 전화 하고 싶다고 가정하면, 전화번호부 에서 엄마의 전화번호(변수)를 찾아서 전화를 건다! 그런 것 처럼 변수는 한마디로 데이터를 쉽게 불러올 수 있도록 이름을 지어준 것이라고 생각하면 쉽다! 원할 때 호출해서 사용하면 된다는 의미이기도 하다!

# 1 variable
name = "MinNi"

print(name)

# 2 variable
name = "Woogi"

print(name)

# 3 variable -> input
name = input("What's u r name? ")

# 4 variable -> output
length = len(name)

print(length)

